package logic;

public class SaveFilePathTo {

	private Integer saveFilePathNo;
	private Integer bdNoRev;
	private String filePath;
	
	public Integer getSaveFilePathNo() {
		return saveFilePathNo;
	}
	public void setSaveFilePathNo(Integer saveFilePathNo) {
		this.saveFilePathNo = saveFilePathNo;
	}
	public Integer getBdNoRev() {
		return bdNoRev;
	}
	public void setBdNoRev(Integer bdNoRev) {
		this.bdNoRev = bdNoRev;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	
}
